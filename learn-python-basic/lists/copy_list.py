# using copy() method
thislist = ["apple", "banana", "cherry"]
mylist = thislist.copy()
print(mylist)

# using list method
thislist = ["apple", "banana", "cherry"]
mylist = list(thislist)
print(mylist)

# using slice operator [:]
thislist = ["apple", "banana", "cherry"]
mylist = thislist[:]
print(mylist)